function halfBaseTimesHeight(base, height) {
    return (base * height) / 2;
}
console.log(halfBaseTimesHeight(1, 1));
console.log(halfBaseTimesHeight(8, 15));
console.log(halfBaseTimesHeight(4, 2));
console.log(halfBaseTimesHeight(11, 7));
console.log(halfBaseTimesHeight(12, 17));
console.log(halfBaseTimesHeight(6, 11.5));
